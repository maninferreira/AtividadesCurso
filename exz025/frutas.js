var fruta = 'laranja'

switch (fruta) {
    case 'laranja':
        console.log('Essa fruta tem a cor laranja.')
        break
    case 'maçã':
        console.log('Essa fruta tem a cor vermelha.')
        break
    case 'limão':
        console.log('Essa fruta tem a cor verde.')
        break
    case 'banana':
        console.log('Essa fruta tem a cor amarela')
        break
    default:
        console.log('Essa fruta tem uma cor desconhecida.')
}